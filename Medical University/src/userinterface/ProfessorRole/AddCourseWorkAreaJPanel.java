    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblPolicies.getModel();
        model.setRowCount(0);
        List<Course> courses = universityEnterprise.getCourseDirectory().getCourses();
        for (Course course : courses) {
            Object[] row = new Object[4];
            row[0] = course;
            row[1] = course.getMedicalPractice();
            row[2] = course.getLearningObjective();
            row[3] = course.getRequirement();
            model.addRow(row);
        }
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tblPolicies.setRowSorter(sorter);
    }

