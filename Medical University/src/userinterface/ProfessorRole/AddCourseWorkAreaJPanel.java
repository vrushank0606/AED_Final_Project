    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
        String courseName = txtCourseName.getText().trim();
        String medicalPractice = txtMedicalPractice.getText().trim();
        String learningObjective = txtLearningObjective.getText().trim();
        String requirement = txtRequirement.getText().trim();
        
        if (courseName.equals("")) {
            JOptionPane.showMessageDialog(null, "Course Name cannot be blank");
            return;
        }

        if (medicalPractice.equals("")) {
            JOptionPane.showMessageDialog(null, "Medical Practice cannot be blank");
            return;
        }

        if (learningObjective.equals("")) {
            JOptionPane.showMessageDialog(null, "Learning Objective cannot be blank");
            return;
        }

        if (requirement.equals("")) {
            JOptionPane.showMessageDialog(null, "Requirementcannot be blank");
            return;
        }

        double varRequirement = 0;
        try {
            varRequirement = Double.parseDouble(requirement);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Input");

            return;
        }

     
        Course course = new Course(courseNumber);
        course.setCourseName(courseName);
        course.setMedicalPractice(medicalPractice);
        course.setLearningObjective(learningObjective);
        course.setRequirement(varRequirement);
        
        
        universityEnterprise.getCourseDirectory().addNewCourse(course);
        JOptionPane.showMessageDialog(null, "Course added successfully");
        
        populateTable();
    }//GEN-LAST:event_btnAddCourseActionPerformed

